/**
 * L'énumération TypeTravail représente les différents types de travaux possibles dans le système.
 * 
 * Les types incluent :
 * <ul>
 *   <li><b>ROUTIER</b> : Travaux liés aux routes et à la voirie.</li>
 *   <li><b>GAZ_ELECTRIQUE</b> : Travaux concernant les réseaux de gaz et d'électricité.</li>
 *   <li><b>CONSTRUCTION_RENOVATION</b> : Travaux de construction ou de rénovation.</li>
 *   <li><b>ENTRETIEN_PAYSAGER</b> : Travaux d'entretien des espaces verts ou paysagers.</li>
 *   <li><b>TRANSPORT_COMMUN</b> : Travaux liés au réseau de transport en commun.</li>
 *   <li><b>SIGNALISATION_ECLAIRAGE</b> : Installation ou maintenance de signalisation et éclairage public.</li>
 *   <li><b>SOUTERRAINS</b> : Travaux effectués dans des infrastructures souterraines.</li>
 *   <li><b>RESIDENTIEL</b> : Travaux réalisés dans des propriétés résidentielles.</li>
 *   <li><b>ENTRETIEN_URBAIN</b> : Travaux d'entretien général dans des zones urbaines.</li>
 *   <li><b>ENTRETIEN_RESEAU_TELECOMMUNICATION</b> : Maintenance des réseaux de télécommunications.</li>
 * </ul>
 */
package com.example.maville.Model;

public enum TypeTravail {
    ROUTIER,
    GAZ_ELECTRIQUE,
    CONSTRUCTION_RENOVATION,
    ENTRETIEN_PAYSAGER,
    TRANSPORT_COMMUN,
    SIGNALISATION_ECLAIRAGE,
    SOUTERRAINS,
    RESIDENTIEL,
    ENTRETIEN_URBAIN,
    ENTRETIEN_RESEAU_TELECOMMUNICATION
}
